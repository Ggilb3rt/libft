#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	t_list	*tmp;

	tmp = NULL;
	tmp->next = lst->next;
	(*del)((t_list*)lst);
	free(lst);
	lst->next = tmp->next;
}
